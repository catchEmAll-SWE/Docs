class Classe:
    def __init__(self,nome):
        self.nome = nome
    
    def setTotale(self, totale):
        self.totale = totale
    
    def setSicure(self, sicure):
        self.sicure = sicure
        
    def setNonSicure(self, nonsicure):
        self.nonsicure = nonsicure
        
    def getNome(self):
        return self.nome
    
    def getTotale(self):
        return self.totale
    
    def getSicure(self):
        return self.sicure
    
    def getNonSicure(self):
        return self.nonsicure
    